"""
AI 简历生成器 - Prompt 模板
============================
集中管理所有 prompt，方便调优和复用。
每个模板对应 resume_engine.py 中的一个核心函数。
"""

from langchain_core.prompts import ChatPromptTemplate


# ============================================================
# 1. 用户信息提取
# ============================================================

USER_INFO_PARSE_PROMPT = ChatPromptTemplate.from_messages([
    ("system", """你是一个简历信息提取专家。从用户提供的非结构化文本中，提取结构化简历信息。

{format_instructions}

提取规则：
- 姓名从文本开头提取
- 联系方式识别邮箱格式和手机号格式
- 技能从"技能"相关描述或工作描述中提取
- 工作经历按时间段拆分为独立的 WorkExperience
- 教育背景合并为一段文字
- 不要编造用户没有提到的信息。如果某字段确实没有提到，用空字符串或空列表"""),
    ("human", "{user_text}"),
])


# ============================================================
# 2. 风格提取
# ============================================================

STYLE_EXTRACTION_PROMPT = ChatPromptTemplate.from_messages([
    ("system", """你是一个简历格式分析专家。分析样本简历的结构风格。

{format_instructions}

分析要点：
- structure：简历包含哪些章节？顺序如何？章节标题用什么格式？
- tone：语言风格是简洁专业、突出成果数据、偏技术术语，还是偏管理？
- format_patterns：具体句式特点，如"用动词开头写成就"、"每段经历 3 条"、"用 STAR 法则"、"时间格式是 YYYY.MM"
- 只基于样本简历实际内容分析，不要编造"""),
    ("human", "样本简历内容：\n\n{resume_text}"),
])


# ============================================================
# 3. JD 要求提取
# ============================================================

JD_REQUIREMENTS_PROMPT = ChatPromptTemplate.from_messages([
    ("system", """你是一个职位分析专家。从招聘 JD 中提取结构化要求。

{format_instructions}

分类标准：
- must_have：明确写出的硬性要求（工作年限、特定技能、学历门槛）
- nice_to_have：加分项、优先条件（"有...优先"、"加分项"）
- keywords：JD 中反复出现的技术名词和业务术语
- hidden_preferences：从措辞中推断的隐性偏好（如"大厂经验优先"暗示偏好有大厂背景的人）"""),
    ("human", "JD 内容：\n\n{jd_text}"),
])


# ============================================================
# 4. 基础简历生成
# ============================================================

BASE_RESUME_PROMPT = ChatPromptTemplate.from_messages([
    ("system", """你是一个专业简历撰写专家。根据用户画像和风格偏好，生成一份完整的简历。

要求：
1. 严格按照风格偏好中定义的 structure 组织章节顺序
2. 遵循风格偏好中定义的 tone 确定语言风格
3. 参考风格偏好中定义的 format_patterns 组织句式
4. 所有内容必须来自用户画像，不要编造任何不存在的信息
5. 用中文撰写
6. 输出格式为 Markdown"""),
    ("human", """请根据以下信息生成简历：

用户画像：
{user_profile}

风格偏好：
{style_profile}"""),
])


# ============================================================
# 5. JD 定制优化
# ============================================================

JD_CUSTOMIZE_PROMPT = ChatPromptTemplate.from_messages([
    ("system", """你是一个简历优化专家。根据 JD 要求，对简历进行针对性修改，提高简历与岗位的匹配度。

优化原则：
1. 保持事实准确——绝对不要编造用户没有的经历或技能
2. 将 JD 要求中用户具备但简历中未突出的技能/经验，调整措辞使其更显眼
3. 自然地融入 JD 关键词到你已有的经历描述中（不要生硬堆砌）
4. 调整经历的描述角度，使其听起来更贴合 JD 要求
5. 保持整体风格和结构一致

JD 要求详情：
- 岗位：{title}
- 必备要求：{must_have}
- 加分项：{nice_to_have}
- 关键词：{keywords}
- 隐性偏好：{hidden_preferences}"""),
    ("human", "简历原文：\n\n{base_resume}"),
])
